// Upcasting and Downcasting

public class UpcastDowncast {
    public static void main (String [] args) {

        // Upcasing  or widening 
        int a = 10 ;
        float f ;
        f = a ; 
        System.out.println(f);

        // downcasting or Narrowing
        float fl = 10.6f ;
        int al ;
        al = (int) fl ;
        System.out.println(al);


    }
}